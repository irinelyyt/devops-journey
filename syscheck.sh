#!/bin/bash
# syscheck.sh ‚Äî –ò—Ä–∏–Ω–∞ –ü–µ—Ç—Ä—ã–∫–∏–Ω–∞, 17.12.2025
# –î–∏–∞–≥–Ω–æ—Å—Ç–∏–∫–∞ —Å–∏—Å—Ç–µ–º—ã: —è—Å–Ω–æ—Å—Ç—å –∑–∞ 5 —Å–µ–∫—É–Ω–¥

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
RED='\033[0;31m'
GREEN='\033[0;32m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# –õ–æ–≥-—Ñ–∞–π–ª
LOG_FILE="syscheck.log"

# –§—É–Ω–∫—Ü–∏—è: –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ + –≤—ã–≤–æ–¥
log() {
    local msg="$1"
    local color="${2:-$NC}"
    echo -e "${color}${msg}${NC}"
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $msg" >> "$LOG_FILE"
}

# –ù–∞—á–∞–ª–æ
log "üîç –ó–∞–ø—É—Å–∫ System Check" "$BLUE"
log "========================" "$BLUE"

# 1. –û–° –∏ —è–¥—Ä–æ
OS=$(lsb_release -ds 2>/dev/null || echo "Ubuntu (WSL)")
KERNEL=$(uname -r)
log "–û–°: $OS" "$GREEN"
log "–Ø–¥—Ä–æ: $KERNEL" "$GREEN"

# 2. Uptime
UPTIME=$(uptime -p 2>/dev/null || echo "unknown")
log "–í—Ä–µ–º—è —Ä–∞–±–æ—Ç—ã: $UPTIME" "$GREEN"

# 3. –ü–∞–º—è—Ç—å
FREE_MEM=$(free -m | awk '/^Mem:/ {printf "%.1f MB", $4}')
log "–°–≤–æ–±–æ–¥–Ω–æ RAM: $FREE_MEM" "$GREEN"

# 4. –î–∏—Å–∫
DISK_FREE=$(df -h / | awk 'NR==2 {print $4}')
log "–°–≤–æ–±–æ–¥–Ω–æ –Ω–∞ /: $DISK_FREE" "$GREEN"

# 5. –¢–æ–ø-3 –ø—Ä–æ—Ü–µ—Å—Å–∞
log "–¢–æ–ø-3 –ø—Ä–æ—Ü–µ—Å—Å–∞ –ø–æ CPU:" "$BLUE"
ps aux --sort=-%cpu | head -n 4 | while read line; do
    log "  $line" "$NC"
done

log "========================" "$BLUE"
log "‚úÖ –ó–∞–≤–µ—Ä—à–µ–Ω–æ. –†–µ–∑—É–ª—å—Ç–∞—Ç –≤ $LOG_FILE" "$GREEN""
